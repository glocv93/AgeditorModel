package ageditormodel;

/**
 *
 * @author Luisp
 */
public class Resource extends Objects {
    
    private int Madera;
    private int Oro;
    private int Piedra;
    private int Alimento;

    public Resource() {
    }
    
    
    
    /**
     * @return the Madera
     */
    public int getMadera() {
        return Madera;
    }

    /**
     * @param Madera the Madera to set
     */
    public void setMadera(int Madera) {
        this.Madera = Madera;
    }

    /**
     * @return the Oro
     */
    public int getOro() {
        return Oro;
    }

    /**
     * @param Oro the Oro to set
     */
    public void setOro(int Oro) {
        this.Oro = Oro;
    }

    /**
     * @return the Piedra
     */
    public int getPiedra() {
        return Piedra;
    }

    /**
     * @param Piedra the Piedra to set
     */
    public void setPiedra(int Piedra) {
        this.Piedra = Piedra;
    }

    /**
     * @return the Alimento
     */
    public int getAlimento() {
        return Alimento;
    }

    /**
     * @param Alimento the Alimento to set
     */
    public void setAlimento(int Alimento) {
        this.Alimento = Alimento;
    }
    
}
